(function( factory ) {
	if ( typeof define === "function" && define.amd ) {

		// AMD. Register as an anonymous module.
		define([
			"jquery",
			"./effect"
		], factory );
	} else {

		// Browser globals
		factory( jQuery );
	}
}(function( $ ) {

if ( $.uiBackCompat !== false ) {
	return $.effects.define( "transfer", function( options, done ) {
		$( this ).transfer( options, done );
	});
}

}));
